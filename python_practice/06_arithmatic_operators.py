# Definition: Arithmetic operators woh symbols hote hain jo programming mein numbers par math ke bunyadi kaam (jaise jama, manfi, zarb, aur taqseem) karne ke liye istemal hote hain.

total_num_of_student = 3

total_num_of_student = total_num_of_student + 1
total_num_of_student += 1

total_num_of_student = total_num_of_student - 2
total_num_of_student -= 1

total_num_of_student = total_num_of_student * 2
total_num_of_student *= 2

total_num_of_student = total_num_of_student / 10
total_num_of_student /= 10

total_num_of_student = total_num_of_student % 3
total_num_of_student %= 3

total_num_of_student = total_num_of_student ** 4
total_num_of_student **= 4


print(total_num_of_student)